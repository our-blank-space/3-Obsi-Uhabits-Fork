export function t(key: keyof typeof translations.en, lang: "auto" | "en" | "es" = "auto"): string {
    let targetLang: "en" | "es" = "en";
    if (lang === "auto") {
        const obsLang = localStorage.getItem("language");
        if (obsLang === "es") targetLang = "es";
    } else {
        targetLang = lang as "en" | "es";
    }
    return (translations[targetLang] as Record<string, string | string[]>)[key] as string || (translations.en as Record<string, string | string[]>)[key] as string || key;
}

export function getWeekdays(lang: "auto" | "en" | "es" = "auto"): string[] {
    let targetLang: "en" | "es" = "en";
    if (lang === "auto") {
        const obsLang = localStorage.getItem("language");
        if (obsLang === "es") targetLang = "es";
    } else {
        targetLang = lang as "en" | "es";
    }
    return translations[targetLang].weekdays;
}
